package day03;

public class Oper1 {
	public static void main(String[] args) {
		boolean itTrue = 10 > 9;
		System.out.println(10==11);
		System.out.println(10 != 11);
		System.out.println(itTrue);
		System.out.println(itTrue && 10==11);
		System.out.println(itTrue || 10==11);
		System.out.println(itTrue && !(10==11));
		
	}
}
